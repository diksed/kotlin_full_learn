package com.sedatkavak.kotlin_full_learn.kotlin_101.oop

class Mathematics {
    fun sum() : Int {
        return 0
    }
    fun sum(number1: Int, number2: Int): Int {
        return number1 + number2
    }
    fun sum(number1: Int, number2: Int, number3: Int): Int {
        return number1 + number2 + number3
    }

}